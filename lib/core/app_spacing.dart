/// Centralised spacing scale for the Minaret app.
/// All EdgeInsets values in screens and widgets should reference these
/// constants instead of raw numbers.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 40.0;

  /// Card inner padding — matches MinaretTheme.cardPadding.
  static const double inset = 20.0;
}
